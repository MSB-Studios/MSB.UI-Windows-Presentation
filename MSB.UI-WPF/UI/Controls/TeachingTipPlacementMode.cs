namespace MSB.UI.Controls
{
    /// <summary>
    /// Specifies the placement mode of a TeachingTip.
    /// </summary>
    public enum TeachingTipPlacementMode
    {
        /// <summary>
        /// The TeachingTip is placed above the target.
        /// </summary>
        Top,
        
        /// <summary>
        /// The TeachingTip is placed below the target.
        /// </summary>
        Bottom,

        /// <summary>
        /// The TeachingTip is placed to the left of the target.
        /// </summary>
        Left,

        /// <summary>
        /// The TeachingTip is placed to the right of the target.
        /// </summary>
        Right,

        /// <summary>
        /// The TeachingTip is placed in the center of the target.
        /// </summary>
        Center
    }
}
