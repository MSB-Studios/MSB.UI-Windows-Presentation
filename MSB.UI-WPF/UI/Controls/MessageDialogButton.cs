namespace MSB.UI.Controls
{
    /// <summary>
    /// Specifies the buttons that are displayed on a message dialog.
    /// </summary>
    public enum MessageDialogButton
    {
        /// <summary>
        /// The dialog displays an OK button.
        /// </summary>
        OK = 0,

        /// <summary>
        /// The dialog displays OK and Cancel buttons.
        /// </summary>
        OKCancel = 1,

        /// <summary>
        /// The dialog displays Yes, No, and Cancel buttons.
        /// </summary>
        YesNoCancel = 3,

        /// <summary>
        /// The dialog displays Yes and No buttons.
        /// </summary>
        YesNo = 4
    }
}
