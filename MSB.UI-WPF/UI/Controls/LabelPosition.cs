namespace MSB.UI.Controls
{
    /// <summary>
    /// Define constants that specify the position of the label in a 'AppBarButton' control.
    /// </summary>
    public enum LabelPosition
    {
        /// <summary>
        /// The label is positioned below the icon.
        /// </summary>
        Top,

        /// <summary>
        /// The label is positioned above the icon.
        /// </summary>
        Left,

        /// <summary>
        /// The label is positioned to the left of the icon.
        /// </summary>
        Right,

        /// <summary>
        /// The label is positioned to the right of the icon.
        /// </summary>
        Bottom
    }
}
